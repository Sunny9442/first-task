// Question 39: City Names: Formatting city-country pairs.
function city_country_pairs(cities, countries) {
    console.log("".concat(cities, " ").concat(countries));
}
city_country_pairs("Karachi", "Pakistan");
city_country_pairs("tokyo", "japan");
city_country_pairs("paris", "france");
city_country_pairs("Atlantica", "America");
