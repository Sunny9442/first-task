// Question 75: Compound Assignment Operators: Use compound assignment operators to simplify arithmetic operations. Start with x = 4 and perform a series of operations (addition, subtraction, multiplication, division) on x using compound operators.
function assignment_operators() {
    var Sun = 4;
    console.log(Sun);
    Sun -= 3;
    console.log("after minus value Sun 4-3 =", Sun);
    Sun += 5;
    console.log("after addition value Sun 1+5 =", Sun);
    Sun *= 10;
    console.log("after multiply value Sun 6*10 =", Sun);
    Sun /= 2;
    console.log("after divide value Sun 60/2 =", Sun);
}
assignment_operators();
